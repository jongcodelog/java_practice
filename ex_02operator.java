public class ex_02operator {
    public static void main(String[] args) {

    
    int num1 = 9;
    int num2 = 5;

    int sum = num1+num2;
    int minus = num1-num2;
    int mul = num1*num2;
    int div = num1/num2;

    System.out.println("더하기 : "+sum);
    System.out.println("빼기 : "+minus);
    System.out.println("곱하기 : "+mul);
    System.out.println("나누기 : "+div);

    }

}